var sort_by = function(field, reverse, primer) {
    var key = function(x) { return primer ? primer(x[field]) : x[field] };

    return function(a, b) {
        var A = key(a),
            B = key(b);
        return ((A < B) ? -1 : ((A > B) ? 1 : 0)) * [-1, 1][+!!reverse];
    }
}